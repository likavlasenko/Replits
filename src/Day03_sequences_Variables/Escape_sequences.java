package Day03_sequences_Variables;

public class Escape_sequences {
    public static void main(String[] args) {

        System.out.println("\tHello World, \n\tMy name is Lika");
        System.out.println("I am in Chicago,\nIllinois \n60614");
        System.out.println("\n\n\t\t\t I'm good at programming");
        System.out.println("\\");   // this prints 1 frontslash
        System.out.println("\\\\"); // this prints 2 front slash
        System.out.println("My name is \'Lika\'");  // 'Lika'
        System.out.println("\"Game of thrones\"");


    }




}
