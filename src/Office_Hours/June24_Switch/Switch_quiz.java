package Office_Hours.June24_Switch;

public class Switch_quiz {
    public static void main(String[] args) {
        int a =10;
        switch(a){

            case 10:
                System.out.println("Monday");
            case 11:
                System.out.println("Tuesday");
            case 12:
                System.out.println("Wednesday");
            default:
                System.out.println("Friday");




        }



    }
}
